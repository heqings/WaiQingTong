                                                   inSection:0];
    UITableViewCell *newCell = [tableView cellForRowAtIndexPath:indexPath];
    if (newCell.accessoryType == UITableViewCellAccessoryNone) {
        newCell.accessoryType = UITableViewCellAccessoryCheckmark;
        newCell.textColor=[UIColor blueColor];
    }
    
    UITableViewCell *oldCell = [tableView cellForRowAtIndexPath:oldIndexPath];
    if (oldCell.accessoryType == UITableViewCellAccessoryCheckmark) {
        oldCell.accessoryType = UITableViewCellAccessoryNone;
        oldCell.textColor=[UIColor blackColor];
    }
    currentIndex=indexPath.row;
    ;
    //
    NSNumber* nu = [[NSNumber alloc]initWithInt:currentIndex];
    NSDictionary *dic = [NSDictionary  dictionaryWithObject:nu forKey:@"currentIndex"];
    //
    [myDelegate onSelectItem:dic Tag:tag];
